#include <stdint.h>
#include "referenceCalculator.h"

uint32_t additionReference (uint32_t a, uint32_t b){
    return a+b;
}

uint32_t substractionReference (uint32_t a, uint32_t b){
    return a-b;
}

uint32_t multiplicationReference (uint32_t a, uint32_t b){
    return a*b;
}

